SmartPort::Switches::operator uint8_t() {

	switch (_acc) {
	case 0x00:
		return _sp.boot();
	case 0x01:
		return _sp.mli(_cpu.a());
	};

	DBG_DISK("smartport: unknown switch: %x", _acc);
	return 0x01;	// error
}
